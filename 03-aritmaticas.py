print(5+15)  # This will print the result of adding 5 and 15
print(15-5)  # This will print the result of subtracting 5 from 15
print(5*15)  # This will print the result of multiplying 5 and 15
print(15/5)  # This will print the result of dividing 15 by 5
print(15//5)  # This will print the result of integer division of 15 by 5
print(15 % 5)  # This will print the remainder of dividing 15 by 5
print(15**5)  # This will print the result of raising 15 to the power of 5
print(10 % 3)  # This will print the remainder of dividing 10 by 3
